def write_file(path: Path, content: str):
    path.parent.mkdir(parents=True, exist_ok=True)
    if not path.exists():
        path.write_text(content.strip() + "\n", encoding="utf-8")

def folder_readme(folder_name: str) -> str:
    return f"""# {folder_name}

## Purpose

This folder contains DMAF assets related to `{folder_name}`.

## Expected Contents

- Markdown documents
- Templates
- Diagrams
- Workbooks
- Supporting references

## Contribution Guidance

Add new files only when they support a DMAF practice domain, lifecycle stage, or reusable capability.

Each major artifact should include metadata where applicable.
"""

