def main():
    root = Path.cwd()

    write_file(root / "README.md", TOP_LEVEL_README)
    write_file(root / "CONTRIBUTING.md", CONTRIBUTING)
    write_file(root / "CHANGELOG.md", CHANGELOG)
    write_file(root / ".gitignore", GITIGNORE)
    write_file(root / "LICENSE", LICENSE)

    for folder in FOLDERS:
        p = root / folder
        p.mkdir(parents=True, exist_ok=True)
        write_file(p / "README.md", folder_readme(folder))
        write_file(p / ".gitkeep", "")

    write_file(root / "03-Assets/Templates/asset-metadata-template.md", ASSET_METADATA_TEMPLATE)
    write_file(root / ".github/ISSUE_TEMPLATE/dmaf-asset-request.md", ISSUE_TEMPLATE)
    write_file(root / ".github/PULL_REQUEST_TEMPLATE.md", PR_TEMPLATE)
    write_file(root / ".github/CODEOWNERS", CODEOWNERS)
    write_file(root / "05-Workbench/CLAUDE.md", CLAUDE)

    print("DMAF repository bootstrap complete.")
    print(f"Version: {VERSION}")
    print(f"Root: {root}")

if __name__ == "__main__":
    main()
